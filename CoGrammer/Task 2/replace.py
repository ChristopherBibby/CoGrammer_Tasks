# Save the sentence as a variable string

sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."

# Use the replace function to replace the ! with a blank space

new_sentence = sentence.replace("!", " ")

# print out the edited sentence

print(new_sentence)

# Use the upper function to turn the sentence into upper case

upper_sentence = new_sentence.upper()

# print out the edited sentence

print(upper_sentence)

# Use slicing to reverse the sentence

reverse_sentence = upper_sentence[::-1]

# print out the edited sentence

print(reverse_sentence)